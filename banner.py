_FONTS = {
    "block": FONT_BLOCK,
    "small": FONT_SMALL
}

_CURRENT_FONT = "block"

def setfont(name):
    global _CURRENT_FONT
    if name in _FONTS:
        _CURRENT_FONT = name
    else:
        raise ValueError(f"Font '{name}' not found.")

def fonts():
    return list(_FONTS.keys())

def add_font(name, font_dict):
    _FONTS[name] = font_dict

def render(text):
    font = _FONTS[_CURRENT_FONT]
    sample_char = list(font.values())[0]
    num_rows = len(sample_char)
    fallback_char = font.get("?", ["?"] * num_rows)
    
    rows = [""] * num_rows
    for char in text:
        char_glyph = font.get(char, fallback_char)
        if len(char_glyph) != num_rows:
            char_glyph = fallback_char
            
        for i in range(num_rows):
            rows[i] += char_glyph[i] + " "
            
    return "\n".join(rows)

def show(text):
    print(render(text))

def main(text_args=None):
    if text_args:
        text = " ".join(text_args) if isinstance(text_args, list) else str(text_args)
        show(text)
    else:
        show("Zeno OS")

def _zeno_cli(args):
    if not args:
        print("Usage: banner <text>")
        return
    main(args)

